// Import mongoose
const mongoose = require('mongoose');

// Create UserSchema
const UserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    date: {
        type: Date,
        default: Date.now,
    },
});

// Export User model
module.exports = User = mongoose.model('user', UserSchema);
